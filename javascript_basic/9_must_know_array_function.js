/**
 * Array Functions
 */

let iveMembers = [ '안유', '가을', '레이', '장원', '리즈', '이서',];

console.log(iveMembers);

// push() - 새로운 값을 맨 끝에다 추가하고 새로운 길이 값을 추가하는 것
iveMembers.push('채원');
console.log(iveMembers.push());
console.log(iveMembers);

console.log('----------');

// pop() - 마지막 값을 삭제 후 반환하는 것
console.log(iveMembers.pop());
console.log(iveMembers);

console.log('----------');

